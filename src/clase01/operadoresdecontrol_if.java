public class operadoresdecontrol_if {

	public static void main(String[] args) {
		
		int a = 17, b = 9;
		
		System.out.println("a > b " + (a > b));
		System.out.println("a < b " + (a < b));
		System.out.println("a >= b " + (a > b));
		System.out.println("a <= b " + (a < b));
		System.out.println("a == b " + (a < b));
		
		
		
		
		

		
		
		

	}

}
