package tp1;

public class ejercicio22for {

	public static void main(String[] args) {

		for(int i = 1; i <= 5; i++){
			System.out.println(+i);



		}
		System.out.println("-------------");


		System.out.println("-------------");
		for(int i = 5; i >= 1; i--) {
			System.out.println(i);

		}






	}










}





